def add(x, y):
	print(f"Adding {x} and {y} results")
	return x+y
	
def sub(x,y):
	print(f"Subtracting {x} and {y} results")
	return x-y
	
def multiply(x,y):
	print(f"Multiply {x} and {y} results")
	return x*y
	
def divide(x,y):
	print(f"divide {x} and {y} results")
	return x/y
	
age=add(30, 5)

height = sub(78, 4)

weight = multiply(90, 2)	

iq = divide(100, 2)

print(f"age is {age} height is {height}, weight is {weight} and iq is {iq}")
	
	